package week1.bitwise;
public class BitWise {

	public static void main(String[] args) {
		System.out.println(4&5);
		System.out.println("=================");
		System.out.println(4|5);
		System.out.println("=================");
		System.out.println(4^5);
		System.out.println("=================");
		System.out.println(~5);
		System.out.println("=================");

	}

}
